import turtle

# Step 2: Create a new turtle. We'll call it "tom"
tom = turtle.Turtle()

# Step 3: Move in the direction Tom's facing for 50 pixels
tom.forward(50)
tom.right(90)     # Rotate clockwise by 90 degrees

tom.forward(50)
tom.right(90)

tom.forward(50)
tom.right(90)

tom.forward(50)
tom.right(90)

# Step 4: We're done!
turtle.done()